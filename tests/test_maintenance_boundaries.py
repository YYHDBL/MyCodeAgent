def test_stable_tools_have_no_runtime_layer_imports():
    violations = []
    for path in (PROJECT_ROOT / "tools").rglob("*.py"):
        tree = ast.parse(path.read_text(encoding="utf-8"), filename=str(path))
        for node in ast.walk(tree):
            if isinstance(node, ast.Import):
                violations.extend(
                    f"{path.relative_to(PROJECT_ROOT)}: {alias.name}"
                    for alias in node.names
                    if alias.name == "runtime" or alias.name.startswith("runtime.")
                )
            elif isinstance(node, ast.ImportFrom) and node.module and (
                node.module == "runtime" or node.module.startswith("runtime.")
            ):
                violations.append(f"{path.relative_to(PROJECT_ROOT)}: {node.module}")

    assert violations == []


