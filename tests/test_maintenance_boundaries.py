def test_stable_product_has_no_agent_teams_boundary_leaks():
    """Agent Teams remains discoverable only through the Git-history archive."""
    stable_roots = ("app", "core", "runtime", "tools", "prompts", "utils", "extensions")
    prohibited_symbols = (
        "experimental.teams",
        "enable_agent_teams",
        "agent_teams",
        "TeamManager",
        "TeamCreate",
        "SendMessage",
        "TeamStatus",
        "TeamDelete",
        "TeamCleanup",
        "TeamApprovals",
        "TeamApprovePlan",
        "TeamFanout",
        "TeamCollect",
        "TeamTask",
        "/team ",
        "/delegate",
    )

    stable_sources = [
        source
        for root in stable_roots
        for source in (PROJECT_ROOT / root).rglob("*.py")
    ]
    leaked_symbols = {
        source.relative_to(PROJECT_ROOT).as_posix(): symbol
        for source in stable_sources
        for symbol in prohibited_symbols
        if symbol in source.read_text(encoding="utf-8")
    }

    assert not leaked_symbols
    assert not list((PROJECT_ROOT / "tools" / "builtin").glob("team_*.py"))
    assert not (PROJECT_ROOT / "tools" / "builtin" / "send_message.py").exists()
    assert not list((PROJECT_ROOT / "prompts" / "tools_prompts").glob("team_*.py"))
    assert not (PROJECT_ROOT / "prompts" / "tools_prompts" / "send_message_prompt.py").exists()
    assert not (PROJECT_ROOT / "experimental" / "teams").exists()

    for user_facing_document in ("README.md", "AGENT.md", "docs/HARNESS.md"):
        contents = (PROJECT_ROOT / user_facing_document).read_text(encoding="utf-8")
        assert "Agent Teams" not in contents
        assert "AgentTeams" not in contents


